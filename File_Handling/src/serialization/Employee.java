package serialization;

import java.io.Serializable;

public class Employee implements Serializable {

    private static final Long serialVersionUID = 10001001L; // It is like a Secret Key
    private int id;
    private String name;
    private transient double salary; // if i add transient to the variable declaration then i wont be serialized

    public Employee(int id, String name, double salary) {
        this.id = id;
        this.name = name;
        this.salary = salary;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    @Override
    public String toString() {
        return "Employee{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", salary=" + salary +
                '}';
    }
}
